func test(val0 int64) {
	fmt.Println("test raw val:", val0, "==========================")

	l := c.Int64.PackLE(val0)
	b := c.Int64.PackBE(val0)

	fmt.Println("Buf(l):", l)
	fmt.Println("Buf(b):", b)

	val1l := c.Int64.UnpackLE(l)
	val1b := c.Int64.UnpackBE(b)

	fmt.Println("NewVal(l):", val1l)
	fmt.Println("NewVal(b):", val1b)

	fmt.Println("")
}

